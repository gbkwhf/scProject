    <h3>三、退换货规则</h3>
    <p>
        3.1 7天之内，当用户取消某个订单时，根据订单支付金额扣减5%费用退回，不支持单个商品的退货;，超过7天的，15天之内的已经参与返利的凡退换货的，扣除各种费用后，并收取订单金额的10%；在确认收货前，其返的积分不可以提现；
    </p>
    <p>3.2 7天之内已经返利的，退换货时扣除各种费用后，收取订单金额的5%；</p>
    <p>3.3 单品出现非人为原因的质量问题退换货，产生的费用由平台承担；</p>
    <p>3.4 超过15天不允许退换货；</p>
    <p>3.5 平台贵重物品不支持退货，如有质量问题可以联系售后做换货处理。</p>
