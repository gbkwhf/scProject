<div class="regulation">
    <h3>二、用户消费返利(积分)规则</h3>
    <p>
        2.1返利以积分的形式，金额和积分按照1:1的比例进行计算，1元＝1消费积分。
    </p>
    <p>2.2日返利模式：用户平台消费即可获得每日返利积分，返利固定期限为一年。</p>
    <p>2.3月返利模式：用户平台消费满一定额度，并提供押金即可获得平台会员身份，会员每月有返利积分。</p>
    <p>②消费积分的计算规则：</p>
    <p>消费积分分为两部分组成，（1）个人消费积分;（2）转介消费贡献积分;</p>
    <p>消费积分（A）=个人消费积分（B）+转介消费积分（C）;</p>
    <p>③消费积分来源计算方式：个人消费积分（B）=用户自身的订单消费总金额（1元=1消费积分）。</p>
    <p>④消费积分的减扣规则：</p>
    <p>A、积分提现：提现收取5%手续费；</p>
    <p>B、积分购物：积分可以在商城上购物只需要加1元钱。</p>
    <p>C、7天之内所返积分冻结，不可提现，不可交易；</p>
    <p>⑤各用户级别升级的结算日期为月末，即无论是新用户或者是具备一定级别的老用户，当月消费积分增加达到升入下一级时，在次月按照新等级计算提成。</p>
    <p>⑥消费金额转化积分的条件：消费金额的订单完成，无退货。</p>
