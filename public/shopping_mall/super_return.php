		<h4 class="listTitle"><em>一、</em>什么是利润共享</h4>
		<p class="explain">在双创共享平台会员区通过现金或线上支付进行下单交易后，其消费的金额，按照利润共享标准，给予相应的返利，由系统每天自动返还。其利润共享的金额显示在会员“可用余额”里，并可以直接提现。</p>
		<h4 class="listTitle"><em>二、</em>利润共享政策</h4>
		<ul class="detail-list">
			<li>
				<em>1、</em>会员自购
				<div class="cont">
					<p>在平台上会员区一次性消费达到1280元（含1280）以上的用户，且订单完成（无退货），方可享有利润共享资格。其返利标准为：每日平台总利润50%÷会员每日订单基数；利润共享天数：180天，
					每天返利的相应的金额根据财务每天数据统计，由系统自动返还到会员的平台账户“可用余额”里。
					<span>备注：以消费1280为基数 ，依次类推。</span></p>
					<ul class="second-level">
						<li>
							<em>1）</em>一次性消费 1280  元    利润共享：日平台总利润50%/1280元*1单
						</li>
						<li>
							<em>2）</em>一次性消费 7680  元    利润共享：日平台总利润50%/1280元*6单
						</li>
						<li>
							<em>3）</em>一次性消费 12800 元    利润共享：日平台总利润50%/1280元*10单
						</li>
						<li>
							<em>4）</em>一次性消费 33280 元    利润共享：日平台总利润50%/1280元*26单
						</li>
						<li>
							<em>5）</em>一次性消费为1280的N倍者，利润共享：日平台总利润50%/1280元*N单,不设上线，多消费多得。
						</li>
					</ul>
				</div>
			</li>
			<li>
				<em>2、</em>推荐共享返利条件
				<div class="cont">
					<ul class="second-level">
						<li>
							<em>1）</em>推荐人要求：必须在平台会员区产生消费并达到返利资格的会员，方可推荐其他用户加入平台并注册。
						</li>
						<li>
							<em>2）</em>被推荐人必须在平台商城消费，且订单完成后（无退货）；
						</li>
						<li>
							<em>3）</em>如推荐人未在平台会员区消费，其被推荐人在会员区消费订单达到基数要求，推荐人将不能享受推荐共享返利。
						</li>
						<li>
							<em>4）</em>推荐共享标准：每日平台（被推荐人平台交易的订单）总利润10% ；
						</li>
						<li>
							<em>5）</em>利润共享天数：永久（备注：只要被推荐人进行线上下单交易就可以享受推荐返利）。
						</li>
					</ul>
					<p>达到以上条件，平台会根据会员个人所推荐的总人数给予会员个人一定比例的推荐返利，推荐共享的金额每天根据财务数据统计，由系统自动返到会员的平台账户“可用余额”里。<span>备注：被推荐人消费达到自购消费基数，同样可享受会员自购利润共享返利。</span></p>
				</div>
			</li>
		</ul>
